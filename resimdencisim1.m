R(1,2)=-cos(fiGrad)*sin(kappaGrad);
R(1,3)=sin(fiGrad);
R(2,1)=cos(omegaGrad)*sin(kappaGrad)+sin(omegaGrad)*sin(fiGrad)*cos(kappaGrad);
R(2,2)=cos(omegaGrad)*cos(kappaGrad)-sin(omegaGrad)*sin(fiGrad)*sin(kappaGrad);
R(2,3)=-sin(omegaGrad)*cos(fiGrad);
R(3,1)=sin(omegaGrad)*sin(kappaGrad)-cos(omegaGrad)*sin(fiGrad)*cos(kappaGrad);
R(3,2)=sin(omegaGrad)*cos(kappaGrad)+cos(omegaGrad)*sin(fiGrad)*sin(kappaGrad);
R(3,3)=cos(omegaGrad)*cos(fiGrad);
 
R

ksip=(ksi0)-c*((R(1,1)*(Xp-Xo)+R(2,1)*(Yp-Yo)+R(3,1)*(Zp-Zo))/(R(1,3)*(Xp-Xo)+R(2,3)*(Yp-Yo)+R(3,3)*(Zp-Zo)));
